class hewan {
    constructor(darat, laut) {
        this.darat = darat;
        this.laut = laut;
    }
}

let hewan1 = new hewan("kucing", "cumi-cumi");

console.log(hewan1.darat + " adalah hewan yang melahirkan dan memiliki 4 kaki dan memiliki bulu");
console.log(hewan1.laut +  " adalah jenis hewan yang memiliki tinta");